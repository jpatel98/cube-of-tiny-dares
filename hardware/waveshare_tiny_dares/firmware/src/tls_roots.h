// =============================================================================
// Root CA certificates for HTTPS connections made by the firmware.
//
// TLS verification behaviour
// --------------------------
// By default the dare client (tiny_dares_client.cpp) verifies the server
// certificate against DARE_ROOT_CA_BUNDLE_PEM below.  If you are developing
// against a local endpoint that uses a self-signed certificate, define the symbol
// TINY_DARES_ALLOW_INSECURE_TLS in your build flags to fall back to
// setInsecure() instead (no verification):
//
//   ; platformio.ini
//   build_flags = -DTINY_DARES_ALLOW_INSECURE_TLS
//
// WARNING: setting that flag disables certificate verification and makes the
// connection vulnerable to man-in-the-middle attacks.  Never use it in
// production firmware.
//
// The geolocation (ipapi.co) and weather (Open-Meteo) endpoints also use HTTPS
// but their connections remain unverified for now — see the comments in
// geolocation.cpp and weather.cpp for the risk assessment.
// =============================================================================

